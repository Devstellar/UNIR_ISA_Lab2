# Realiza la suma de los argumentos a y b
def suma(a, b):
  return(a + b)

# Realiza la resta sobre el argumento a de b
def resta(a, b):
  return(a - b)

# Realiza la multiplicación de a y b
def multiplicacion(a, b):
  return(a*b)

# Realiza la división de a por b
def division(a, b):
  return(a / b)
